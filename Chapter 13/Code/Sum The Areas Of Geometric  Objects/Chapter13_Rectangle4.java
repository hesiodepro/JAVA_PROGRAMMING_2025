public class Chapter13_Rectangle4 extends Chapter13_GeometricObjects7 {
    private double width;
    private double height;

    public Chapter13_Rectangle4(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    @Override
    public double getArea() {
        return width * height;  
    }
}

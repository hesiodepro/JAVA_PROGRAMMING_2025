public class Chapter1_Area_Perimeter {
    public static void main(String[] args) {
        
        double pi = 3.14159;
        double radius = 5.5;
        double area = radius * radius * pi;
        double perimeter = 2 * radius * pi;
        
        // Display the output
        System.out.println("The Area is " + area + " And the Perimeter is " + perimeter);
    }
}

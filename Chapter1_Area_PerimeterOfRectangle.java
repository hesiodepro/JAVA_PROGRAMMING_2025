public class Chapter1_Area_PerimeterOfRectangle {
    public static void main(String[] args) {
        
        double width = 4.5;
        double height = 7.9;
        double area = width * height;
        double perimeter = 2 * (width + height);
        
        // Display the output
        System.out.println("The Area  of a rectangle is " + area + " And the Perimeter is " + perimeter);
    }
}
